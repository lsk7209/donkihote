async function seedGuideContent() {
  console.log('가이드 콘텐츠 삽입 시작...');

  for (const content of guideContents) {
    try {
      // 기존 게시글 확인
      const existing = await db
        .select()
        .from(posts)
        .where(eq(posts.slug, content.slug))
        .limit(1);

      if (existing.length > 0) {
        console.log(`이미 존재하는 게시글: ${content.slug}`);
        continue;
      }

      // 새 게시글 삽입
      await db.insert(posts).values({
        slug: content.slug,
        title: content.title,
        content: content.content,
        category: content.category,
        isPublished: true,
        createdAt: new Date().toISOString(),
      });

      console.log(`✓ 삽입 완료: ${content.slug}`);
    } catch (error) {
      console.error(`✗ 삽입 실패: ${content.slug}`, error);
    }
  }

  console.log('가이드 콘텐츠 삽입 완료!');
}

// 스크립트 실행
